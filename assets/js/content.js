  // ============================================================
  // 引擎 API
  // ============================================================

  function poleOf(dim, score) {
    // score < 5 → 左极；> 5 → 右极；接近 5 → 中
    if (score < 4) return { side: "L", strength: (5 - score) };
    if (score > 6) return { side: "R", strength: (score - 5) };
    return { side: "M", strength: 0 };  // balanced
  }

  function charFor(dimId, side) {
    return POLE[dimId][side === "L" ? "L" : "R"].char;
  }

  function adjFor(dimId, side, pickIdx) {
    const pole = POLE[dimId][side === "L" ? "L" : "R"];
    return pole.adjs[pickIdx % pole.adjs.length];
  }

  // 8-char 人格代码："守独谋远钢冷义隐"
  function buildCode(userVec) {
    return Object.keys(POLE).map(d => {
      const { side } = poleOf(d, userVec[d]);
      if (side === "M") return POLE[d].L.char;   // center defaults to left char (arbitrary)
      return charFor(d, side);
    }).join("");
  }

  // 类型名：基于最强 3 维
  function buildTypeName(userVec) {
    const sortedByStrength = Object.keys(POLE).map(d => {
      const { side, strength } = poleOf(d, userVec[d]);
      return { dim: d, side, strength };
    }).sort((a, b) => b.strength - a.strength);

    const top3 = sortedByStrength.slice(0, 3);
    if (top3[0].strength < 0.6) return "中和平衡之人";

    // pick adj for each top dim with varied index for diversity
    const a1 = adjFor(top3[0].dim, top3[0].side, Math.floor(top3[0].strength * 1.1) % 4);
    const a2 = adjFor(top3[1].dim, top3[1].side, Math.floor(top3[1].strength * 1.3) % 4);
    const a3 = adjFor(top3[2].dim, top3[2].side, Math.floor(top3[2].strength * 1.7) % 4);
    return `${a1}${a2}的${a3}者`;
  }

  // 每维深度解读：返回 { para, similarHeroes }
  function buildDimInterpret(dimId, score, heroes) {
    const { side, strength } = poleOf(dimId, score);
    const key = strength < 1 ? "mid" : (side === "L" ? "L_strong" : "R_strong");
    const para = DIM_INTERPRET[dimId][key];

    // find 2 heroes nearest to user's value on this dim
    const similar = heroes
      .map(h => ({ hero: h, delta: Math.abs((h.vec[dimId] || 5) - score) }))
      .sort((a, b) => a.delta - b.delta)
      .slice(0, 2)
      .map(x => x.hero.name);

    return { para, similarHeroes: similar };
  }

  // 本质画像：300-400 字段落合成
  function buildEssence(userVec) {
    const matched = ESSENCE_FRAGMENTS.filter(f => f.when(userVec));
    // de-dup if multiple fragments are identical
    const uniqTexts = [...new Set(matched.map(f => f.text))];
    // pick up to 6 fragments
    const pick = uniqTexts.slice(0, Math.min(6, uniqTexts.length));
    if (pick.length === 0) return "你这个人各方面都比较平衡——不走极端本身就是一种力量。在变化多端的世界里，平衡的人最难被推翻。";

    const connectors = ["", "同时，", "另一方面，", "值得一提的是，", "还有一点，", "最后，"];
    return pick.map((p, i) => connectors[i % connectors.length] + p).join("");
  }

  // 5 场景预测：每场景根据用户主导维度选一条
  function buildScenarios(userVec) {
    // sort dims by strength of lean
    const ranked = Object.keys(POLE).map(d => {
      const { side, strength } = poleOf(d, userVec[d]);
      return { dim: d, side, strength, key: `${d}-${side}` };
    }).sort((a, b) => b.strength - a.strength);

    const out = {};
    for (const [scenarioName, texts] of Object.entries(SCENARIOS)) {
      // try top-ranked leans in order, pick the first that has a text defined
      let chosen = null;
      for (const r of ranked) {
        if (r.strength < 0.6) break;  // too balanced, use fallback
        if (texts[r.key]) { chosen = texts[r.key]; break; }
      }
      out[scenarioName] = chosen || "你在这方面没有明显的偏好——或许取决于场合和对象，你的应对会灵活。";
    }
    return out;
  }

  // 优势 / 盲点 / 驱动
  function buildSWE(userVec) {
    const ranked = Object.keys(POLE).map(d => {
      const { side, strength } = poleOf(d, userVec[d]);
      return { dim: d, side, strength, char: charFor(d, side) };
    }).sort((a, b) => b.strength - a.strength);

    if (ranked[0].strength < 0.6) {
      return {
        strength: "你各维度均衡——不走极端就是你的力量，遇到任何局面都不至于崩盘。",
        blindspot: "平衡也意味着缺少标志性特质——别人记不住你是哪一型，你也容易失去自我锚点。",
        engine:    "平衡 · 在任何极端里都能找回中道。",
      };
    }

    const topChar = ranked[0].char;
    const topChar2 = ranked[1].char;
    return {
      strength:  POLE_STRENGTH[topChar]  || "——",
      blindspot: POLE_BLINDSPOT[topChar] || "——",
      engine:    `${POLE_ENGINE[topChar] || "—"} · ${POLE_ENGINE[topChar2] || "—"} —— 这两股力量驱动你走到今天。`,
    };
  }

